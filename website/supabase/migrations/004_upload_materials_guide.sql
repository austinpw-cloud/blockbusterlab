-- Stage 9 재설계 — 업로드 자료 가이드 deliverable 추가
--
-- 배경:
--   1차 업로드 자료 평가 결과 ASO 품질 기준 미달일 경우,
--   개발자에게 "어떤 자료를 추가 촬영·업로드해야 하는지" 한국어 마크다운 가이드를
--   결과물로 제공한다. 기존 aso_guide는 ASO 전략 가이드 용도로 예약되어 있으므로
--   업로드 보완 안내는 별도 enum 값으로 분리한다.
--
-- ⚠️ 실행 주의 (PostgreSQL 제약):
--   `ALTER TYPE ... ADD VALUE` 로 추가된 enum 값은 같은 트랜잭션 내에서는
--   참조할 수 없다. 이 파일은 새 값을 추가만 하고 참조하지 않으므로 단독 실행은 안전하다.
--   단, 다른 마이그레이션과 같은 트랜잭션에서 실행되고 그 마이그레이션이 새 값을 즉시
--   사용하면 실패한다. Supabase CLI/Dashboard 에서 이 파일은 단독 마이그레이션으로 실행할 것.
-- =====================================================================

ALTER TYPE deliverable_type ADD VALUE IF NOT EXISTS 'upload_materials_guide';
